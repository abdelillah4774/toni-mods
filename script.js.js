const modsList = document.getElementById("mods-list");
const searchInput = document.getElementById("searchInput");

function displayMods(modsToDisplay) {
  modsList.innerHTML = "";
  modsToDisplay.forEach(mod => {
    const card = document.createElement("div");
    card.className = "mod-card";
    card.innerHTML = `
      <img src="${mod.image}" alt="${mod.name}" />
      <div class="mod-info">
        <h3>${mod.name}</h3>
        <p>${mod.description}</p>
        <button class="download-btn" onclick='openDetails(${JSON.stringify(mod)})'>dawload</button>
      </div>
    `;
    modsList.appendChild(card);
  });
}

function openDetails(mod) {
  // حفظ بيانات المود في localStorage
  localStorage.setItem("modDetails", JSON.stringify(mod));
  // فتح صفحة جديدة
  const win = window.open();
  win.document.write(`
    <html>
      <head>
        <title>${mod.name}</title>
        <style>
          body { font-family: Arial; background: #121212; color: white; text-align: center; padding: 20px; }
          img { max-width: 300px; border-radius: 10px; }
          a { padding: 10px 20px; background: #4CAF50; color: white; text-decoration: none; border-radius: 6px; display: inline-block; margin-top: 20px; }
        </style>
      </head>
      <body>
        <h2>${mod.name}</h2>
        <img src="${mod.image}" />
        <p>${mod.description}</p>
        <a href="${mod.file}" target="_blank">dawnload 
  mod</a>
      </body>
    </html>
  `);
}

searchInput.addEventListener("input", () => {
  const q = searchInput.value.toLowerCase();
  const filtered = mods.filter(mod =>
    mod.name.toLowerCase().includes(q) || mod.description.toLowerCase().includes(q)
  );
  displayMods(filtered);
});

displayMods(mods);
